function enhanceMobilePageMenu() {
  const siteNav = document.querySelector(".site-nav");
  if (!siteNav || document.querySelector(".mobile-page-menu")) return;

  const links = Array.from(siteNav.querySelectorAll("a"));
  if (!links.length) return;

  const wrapper = document.createElement("label");
  wrapper.className = "mobile-page-menu";

  const label = document.createElement("span");
  label.textContent = "Choose page";

  const select = document.createElement("select");
  select.setAttribute("aria-label", "Choose page");

  const currentUrl = new URL(window.location.href);
  let selectedIndex = 0;

  links.forEach((link, index) => {
    const option = document.createElement("option");
    const linkUrl = new URL(link.href, window.location.href);
    const linkText = link.textContent.trim();

    option.value = linkUrl.href;
    option.textContent = linkText;
    select.appendChild(option);

    if (link.classList.contains("active") || samePage(linkUrl, currentUrl)) {
      selectedIndex = index;
    }
  });

  select.selectedIndex = selectedIndex;
  select.addEventListener("change", () => {
    window.location.href = select.value;
  });

  wrapper.append(label, select);
  siteNav.after(wrapper);
  document.body.classList.add("mobile-nav-ready");
}

function ensureSiteFlowFooter() {
  const currentUrl = new URL(window.location.href);
  const currentIndex = SITE_FLOW.findIndex((entry) => samePage(entryUrl(entry, currentUrl), currentUrl));
  if (currentIndex < 0) return;

  document.querySelectorAll("nav.footer-nav, nav.pager, nav.site-flow-nav").forEach((nav) => {
    nav.remove();
  });

  const previous = SITE_FLOW[(currentIndex - 1 + SITE_FLOW.length) % SITE_FLOW.length];
  const next = SITE_FLOW[(currentIndex + 1) % SITE_FLOW.length];
  const siteMap = SITE_FLOW.find((entry) => entry.path === "site-map.html");

  const nav = document.createElement("nav");
  nav.className = "site-flow-nav";
  nav.setAttribute("aria-label", "Site flow navigation");

  nav.append(
    siteFlowCard("Previous", previous, currentUrl),
    siteFlowCard("Site map", siteMap, currentUrl, "site-flow-index"),
    siteFlowCard("Next", next, currentUrl, "next")
  );

  const target = document.querySelector("main") || document.body;
  target.appendChild(nav);
}

function siteFlowCard(kicker, entry, currentUrl, extraClass = "") {
  const link = document.createElement("a");
  link.className = `site-flow-card ${extraClass}`.trim();
  link.href = entryUrl(entry, currentUrl).href;

  const span = document.createElement("span");
  span.textContent = kicker;

  const strong = document.createElement("strong");
  strong.textContent = entry.label;

  const small = document.createElement("small");
  small.textContent = entry.summary;

  link.append(span, strong, small);
  return link;
}

function renderSiteMapPage() {
  const container = document.querySelector("[data-site-map-sections]");
  if (!container) return;

  const currentUrl = new URL(window.location.href);
  const grouped = SITE_FLOW.reduce((map, entry) => {
    if (!map.has(entry.section)) map.set(entry.section, []);
    map.get(entry.section).push(entry);
    return map;
  }, new Map());

  container.innerHTML = "";

  grouped.forEach((entries, section) => {
    const sectionEl = document.createElement("section");
    sectionEl.className = "site-map-section";

    const heading = document.createElement("h2");
    heading.textContent = section;

    const grid = document.createElement("div");
    grid.className = "site-map-grid";

    entries.forEach((entry) => {
      const card = document.createElement("a");
      card.className = "site-map-card";
      card.href = entryUrl(entry, currentUrl).href;

      const label = document.createElement("strong");
      label.textContent = entry.label;

      const path = document.createElement("span");
      path.textContent = entry.path;

      const summary = document.createElement("p");
      summary.textContent = entry.summary;

      card.append(label, path, summary);
      grid.appendChild(card);
    });

    sectionEl.append(heading, grid);
    container.appendChild(sectionEl);
  });

  const externalContainer = document.querySelector("[data-site-map-external]");
  if (!externalContainer) return;

  const externalGrid = document.createElement("div");
  externalGrid.className = "site-map-grid";

  SITE_MAP_EXTERNAL_LINKS.forEach((entry) => {
    const card = document.createElement("a");
    card.className = "site-map-card";
    card.href = entry.href;

    const label = document.createElement("strong");
    label.textContent = entry.label;

    const path = document.createElement("span");
    path.textContent = entry.href;

    const summary = document.createElement("p");
    summary.textContent = entry.summary;

    card.append(label, path, summary);
    externalGrid.appendChild(card);
  });

  externalContainer.appendChild(externalGrid);
}

function entryUrl(entry, currentUrl = new URL(window.location.href)) {
  return new URL(`${rootPrefix(currentUrl.pathname)}${entry.path}`, currentUrl);
}

function normalisePath(pathname) {
  return pathname.replace(/\/index\.html$/, "/").replace(/\/$/, "/index.html");
}

function samePage(linkUrl, currentUrl) {
  return (
    linkUrl.origin === currentUrl.origin &&
    normalisePath(linkUrl.pathname) === normalisePath(currentUrl.pathname)
  );
}

function fileName(href) {
  const url = new URL(href, window.location.href);
  return url.pathname.split("/").pop() || "index.html";
}

function isBuilderLink(href, builderFiles) {
  const url = new URL(href, window.location.href);
  return url.pathname.includes("/builders/") && builderFiles.has(fileName(href));
}

function rootPrefix(pathname) {
  const parts = pathname.split("/").filter(Boolean);
  const lastPart = parts[parts.length - 1] || "";
  const currentFolder = lastPart.includes(".") ? parts[parts.length - 2] : lastPart;
  return ["builders", "episodes", "recording-tools"].includes(currentFolder) ? "../" : "";
}

